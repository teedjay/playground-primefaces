package teedjay;

/**
 * A simple data structure - contained in a POJO
 *
 * @author thore
 */
public class UserData {

    private String name;
    private String city;
    private String code;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
